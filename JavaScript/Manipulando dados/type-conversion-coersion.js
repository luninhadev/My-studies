/*
    Type conversion (typecasting) vs Type coersion

    * Alteração de um tipo de dado para outro tipo
*/

console.log('9' + 5) // coersion: aqui o JS forçou o 5 a ser do tipo string pra não ocorrer nenhum problema no código.

console.log(Number('9') + 5) // conversion: aqui nós mudamos o tipo manualmente