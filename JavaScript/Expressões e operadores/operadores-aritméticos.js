
// Operadores Aritméticos

// multiplicação
console.log(3.2 * 5)
// divisão
console.log(12 / 2)
// soma
console.log(34 + 67)
console.log(12 - 8)

// resto da divisão %
let remainder
remainder = 11 % 3
console.log(remainder)

// incremento ++
let increment = 0
increment++
console.log(increment)
console.log(increment++) // aqui ele incrementa depois de renderizar o valor
console.log(++increment) // aqui ele incrementa antes de renderizar na tela

// decremento --
let decrement = 0
console.log(decrement--)
console.log(--decrement)

// exponencial **
console.log(3 ** 3) // 3 elevado a 3
